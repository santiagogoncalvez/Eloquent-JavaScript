// Objeto math


// Math.max(): devuelve el mayor numero de los valores dados
console.log(Math.max(1, 2, 3, 4));   // 4


// Math.min(): devuelve el menor numero de los valores dados
console.log(Math.min(1, 2, 3, 4));   //1


// Math.sqrt(): devuelve la raiz cuadrada del numero dado
console.log(Math.sqrt(9));   // 3


// Math.PI: contiene la aproximacion mas cercana al numero π(pi)
console.log(Math.PI);   // 3.141592653589793


// Math.random(): retorna un nuevo numero pseudoaleatorio entre 0 (inclusivo) y 1 (exclusivo)
console.log(Math.random());


// Math.floor(): redondea hacia abajo el numero que se le pase
console.log(Math.floor(1.4))   // 1
console.log(Math.floor(1.7));  // 1


// Math.ceil(): redondea hacia arriba el numero que se le pase
console.log(Math.ceil(2.3))   // 3
console.log(Math.ceil(2.6))   // 3


// Math.round(): redondea al entero más cercano del numero que se le pase
console.log(Math.round(1.6))   // 2
console.log(Math.round(1.4))   // 1


// Math.abs(): toma el valor absoluto del numero que se le pase
console.log(Math.abs(-2))   // 2